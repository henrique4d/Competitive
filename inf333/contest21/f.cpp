struct FenwickTree {
    vector<int> bit;  // binary indexed tree
    int n;

    FenwickTree(int n) {
        this->n = n;
        bit.assign(n, 0);
    }

    FenwickTree(vector<int> const &a) : FenwickTree(a.size()) {
        for (size_t i = 0; i < a.size(); i++)
            add(i, a[i]);
    }

    int sum(int r) {
        int ret = 0;
        for (; r >= 0; r = (r & (r + 1)) - 1)
            ret += bit[r];
        return ret;
    }

    int sum(int l, int r) {
        return sum(r) - sum(l - 1);
    }

    void add(int idx, int delta) {
        for (; idx < n; idx = idx | (idx + 1))
            bit[idx] += delta;
    }
};

void solve(){
    int n;
    cin >> n;
    vector<ll> app(n+10);
    int x;
    for (int i=0; i<n; i++){
        cin >> x;
        app[x] = i;
    }

    ll l = INF;
    ll r = -1;
    FenwickTree ft(n+10);

    for (int i=1; i<=n; i++){
        l = min(l, app[i]);
        r = max(r, app[i]);
        ft.add(app[i], 1);
        if (ft.sum(l,r) == r-l+1) cout << 1;
        else cout << 0;
    }
    cout << endl;
}
 
