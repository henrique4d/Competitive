const ll MAXN = 2e5+10;
ll a[MAXN];
pair<ll,ll> pos[MAXN];
ll n;

struct Bit {
	int n;
	vector<ll> bit;
	Bit(int _n=0) : n(_n), bit(n + 1) {}
	Bit(vector<int>& v) : n(v.size()), bit(n + 1) {
		for (int i = 1; i <= n; i++) {
			bit[i] += v[i - 1];
			int j = i + (i & -i);
			if (j <= n) bit[j] += bit[i];
		}
	}
	void update(int i, ll x) { // soma x na posicao i
		for (i++; i <= n; i += i & -i) bit[i] += x;
	}
	ll pref(int i) { // soma [0, i]
		ll ret = 0;
		for (i++; i; i -= i & -i) ret += bit[i];
		return ret;
	}
	ll query(int l, int r) {  // soma [l, r]
		return pref(r) - pref(l - 1); 
	}
	int upper_bound(ll x) {
		int p = 0;
		for (int i = __lg(n); i+1; i--) 
			if (p + (1<<i) <= n and bit[p + (1<<i)] <= x)
				x -= bit[p += (1 << i)];
		return p;
	}
};

void solve(){
    cin >> n;
    Bit fenw(n+10);
    for (int i=0; i<n; i++){
        cin >> a[i];
        pos[i] = {-a[i], i};
    }
    sort(pos, pos+n);
    ll ans = INFLL;
    for (int i=0; i<n; i++){
        ans = min (ans, pos[i].second + fenw.query(pos[i].second,n+5));
        fenw.update(pos[i].second,1);
    }
    cout << ans << endl;
}

int main() {
    optimize; 
    ll T = 1;
    cin >> T;
    while(T--) {
        solve();
    }
    return 0;
}