ll DP(ll pos, ll val){
    if (pos >= n) return 0;
    if (dp[pos][val] != -1) return dp[pos][val];

    ll resp = 0;
    if (s[pos] == '?'){
        for (int i=0; i<26; i++){
            resp = max(resp, DP(pos+1, aut[val][i]) + (aut[val][i] == t.size()));
        }
        return dp[pos][val] = resp;
    }
    else{
        resp = max(resp,DP(pos+1, aut[val][s[pos] - 'a' ]) + (aut[val][s[pos] - 'a'] == t.size()));
        return dp[pos][val] = resp;
    }
}

