long long compute_hash(string const& s) {
    const int p = 31;
    const int m = 1e9 + 9;
    long long hash_value = 0;
    long long p_pow = 1;
    for (char c : s) {
        hash_value = (hash_value + (c - 'a' + 1) * p_pow) % m;
        p_pow = (p_pow * p) % m;
    }
    return hash_value;
}

long long compute_hash2(string const& s) {
    const int p = 56;
    const int m = 1e9 + 7;
    long long hash_value = 0;
    long long p_pow = 1;
    for (char c : s) {
        hash_value = (hash_value + (c - 'a' + 1) * p_pow) % m;
        p_pow = (p_pow * p) % m;
    }
    return hash_value;
}

pair<pair<ll,ll>,pair<ll,ll>> a[30];

void solve(){
    int n;
    cin >> n;
    string genero, artist;
    for (int i=0; i<n; i++){
        cin >> genero >> artist;

        a[i] = {{compute_hash(genero), compute_hash2(genero)}, {compute_hash(artist), compute_hash2(artist)}};
    }

    ll maior = 0;

    for (int i=0; i<(1<<n); i++){
        pair<pair<ll,ll>,pair<ll,ll>> last = {{-1,-1},{-1,-1}};
        ll cont = 0;
        bool possible = true;

        for (int j=0; j<n; j++){
            if (i&(1<<j)){
                if (last.first == make_pair((ll)-1,(ll)-1) or last.first == a[j].first or last.second == a[j].second){
                    last = a[j];
                    cont++;
                }
                else{
                    possible = false;
                    break;
                }
            }
        }
        if (possible){
            maior = max(maior, cont);
        }
    }

    cout << n - maior << endl;
}
 
